package SecondTry.Lessons_Java.pt1_Beginner.Lessons46_Serialization;

public class CatChild extends Cat{
    public CatChild(String NAME, int age, int cup) {
        super(NAME, age, cup);
    }

    @Override
    public String getNAME() {
        return super.getNAME();
    }

    @Override
    public int getAge() {
        return super.getAge();
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
